#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*x;

	i = 0;
	x = s;
	if (n != 0)
	{
		while (i < n)
		{
			x[i] = 0;
			s = x;
			i++;
		}
	}
}
